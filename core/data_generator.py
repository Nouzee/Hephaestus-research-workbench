"""
BTC L2 Data Generator — loads pre-built events_features.parquet (from 高频项目),
normalises, generates labels, and outputs daily parquet files for concat.

The events_features.parquet is generated by L2DataLoader + 高频项目 feature pipeline.
Columns: ts_ms, mid_px, spread, imbalance, total_depth, bid_px, ask_px,
         bid_sz, ask_sz, signed_imbalance, abs_imbalance, spread_ticks,
         duration_ms, waiting_time_ms, + more
"""
import os
import re
import numpy as np
import pandas as pd
from pathlib import Path
from typing import List, Dict, Tuple, Optional


# Feature columns matching 高频项目 train_model.py FEATURE_COLS
# These are the columns we use for training.
FEATURE_COLS = [
    "mid_px",
    "spread",
    "imbalance",
    "total_depth",
    "bid_px",
    "ask_px",
    "bid_sz",
    "ask_sz",
    "signed_imbalance",
    "abs_imbalance",
    "spread_ticks",
]


def load_events_parquet(parquet_path: str) -> pd.DataFrame:
    """Load pre-built events_features.parquet."""
    if not os.path.exists(parquet_path):
        raise FileNotFoundError(f"events_features.parquet not found: {parquet_path}")

    df = pd.read_parquet(parquet_path)
    df = df.sort_values("ts_ms").reset_index(drop=True)

    # Verify columns
    missing = [c for c in FEATURE_COLS if c not in df.columns]
    if missing:
        raise ValueError(f"Missing columns in {parquet_path}: {missing}")

    return df


def split_by_date(df: pd.DataFrame, date_range: Tuple[str, str]) -> pd.DataFrame:
    """
    Chronologically split events by date range.
    df must have 'ts_ms' column (unix ms timestamps).
    date_range: (start_date_str, end_date_str), e.g., ("2026-01-01", "2026-01-22")
    """
    start_ms = pd.Timestamp(date_range[0]).value // 1_000_000  # ns -> ms
    end_ms = pd.Timestamp(date_range[1]).value // 1_000_000 + 86_400_000  # end of day

    mask = (df["ts_ms"] >= start_ms) & (df["ts_ms"] < end_ms)
    return df[mask].copy()


def split_by_days(df: pd.DataFrame) -> List[Tuple[str, pd.DataFrame]]:
    """
    Split events into per-day chunks based on ts_ms.
    Returns list of (date_str, df_day).
    """
    # Convert ts_ms to date string
    df = df.copy()
    df["_date"] = pd.to_datetime(df["ts_ms"], unit="ms").dt.strftime("%Y-%m-%d")

    days = []
    for date_str, g in df.groupby("_date", sort=True):
        g = g.drop(columns=["_date"]).reset_index(drop=True)
        days.append((date_str, g))

    return days


def normalize_daily(df: pd.DataFrame, stats_history: List[Tuple]) -> Tuple[pd.DataFrame, List]:
    """
    Normalize features using rolling 5-day statistics.
    stats_history: list of (n_rows, sum, sum2) tuples.
    """
    x = df[FEATURE_COLS].to_numpy(dtype=np.float64)

    n_cur = x.shape[0]
    cur_sum = np.sum(x, axis=0)
    cur_sum2 = np.sum(x * x, axis=0)

    window = min(5, len(stats_history))
    if window == 0:
        use_n, use_sum, use_sum2 = n_cur, cur_sum, cur_sum2
    else:
        hist = stats_history[-window:]
        use_n = sum(h[0] for h in hist)
        use_sum = np.sum([h[1] for h in hist], axis=0)
        use_sum2 = np.sum([h[2] for h in hist], axis=0)

    use_mean = use_sum / max(use_n, 1)
    use_var = use_sum2 / max(use_n, 1) - use_mean ** 2
    use_var = np.maximum(use_var, 0.0)
    use_std = np.sqrt(use_var)

    df_norm = df.copy()
    eps = 1e-12
    for i, col in enumerate(FEATURE_COLS):
        if use_std[i] > eps:
            df_norm[col] = (df[col] - use_mean[i]) / use_std[i]
        else:
            df_norm[col] = 0.0

    stats_history.append((n_cur, cur_sum, cur_sum2))
    return df_norm, stats_history


def compute_labels(df: pd.DataFrame, k: int, H: float,
                   use_log_return: bool = False) -> np.ndarray:
    """
    Forward return label.

    For each row t:
        m_minus = mean(mid[t-k+1 : t+1])
        m_plus  = mean(mid[t+1 : j]) where ts_ms[j-1] <= ts_ms[t] + H*1000
        label   = (m_plus - m_minus) / m_minus
    """
    mid = df["mid_px"].to_numpy(dtype=np.float64)
    # "Time" column = ts_ms (renamed in generate_day_data)
    ts = df["Time"].to_numpy(dtype=np.float64) if "Time" in df.columns else df["ts_ms"].to_numpy(dtype=np.float64)
    N = len(mid)
    y = np.full(N, np.nan, dtype=np.float32)

    if N < k + 1:
        return y

    csum = np.zeros(N + 1, dtype=np.float64)
    csum[1:] = np.cumsum(mid, dtype=np.float64)

    t_start = k - 1
    t_candidates = np.arange(t_start, N - 1, dtype=np.int64)
    m_minus = (csum[t_candidates + 1] - csum[t_candidates - k + 1]) / float(k)

    H_ms = float(H) * 1000.0   # convert seconds to ms

    j = t_start + 1
    for idx, t in enumerate(t_candidates):
        if j < t + 1:
            j = t + 1

        end_time = ts[t] + H_ms
        while j < N and ts[j] <= end_time:
            j += 1

        n = j - (t + 1)
        if n <= 0:
            continue

        mp = m_minus[idx]
        if not np.isfinite(mp) or mp <= 0:
            continue

        m_plus = (csum[j] - csum[t + 1]) / float(n)
        if not np.isfinite(m_plus) or m_plus <= 0:
            continue

        if use_log_return:
            r = np.log(m_plus / mp)
        else:
            r = (m_plus - mp) / mp

        y[t] = np.float32(r)

    return y


def generate_day_data(
    df_day: pd.DataFrame,
    date_str: str,
    stats_history: List[Tuple],
    output_lob_parquet: str,
    output_label_npy: str,
    label_k: int = 100,
    label_h: float = 10.0,
    use_log_return: bool = False,
) -> dict:
    """Generate one day's LOB parquet + label npy."""
    if len(df_day) == 0:
        raise ValueError(f"Empty data for {date_str}")

    # Build output DataFrame: Time, TimeDiff, then features
    ts = df_day["ts_ms"].to_numpy(dtype=np.float64)
    dt = np.empty_like(ts)
    dt[0] = 0.0
    dt[1:] = np.maximum(ts[1:] - ts[:-1], 0.0)
    dt = np.log1p(dt)

    out = pd.DataFrame()
    out["Time"] = ts
    out["TimeDiff"] = dt.astype(np.float32)

    for col in FEATURE_COLS:
        out[col] = df_day[col].to_numpy(dtype=np.float32)

    out = out.fillna(0.0)

    # Labels MUST be computed BEFORE normalization (mid_px must stay >0)
    y = compute_labels(out, k=label_k, H=label_h, use_log_return=use_log_return)

    # Normalize features (excludes Time/TimeDiff)
    out_norm, stats_history = normalize_daily(out, stats_history)

    # Save LOB
    os.makedirs(os.path.dirname(os.path.abspath(output_lob_parquet)), exist_ok=True)
    out_norm.to_parquet(output_lob_parquet, index=False)
    os.makedirs(os.path.dirname(os.path.abspath(output_label_npy)), exist_ok=True)
    np.save(output_label_npy, y)

    y_flat = np.asarray(y).reshape(-1)
    finite_mask = np.isfinite(y_flat)
    valid_y = y_flat[finite_mask]

    label_dist = {
        "total_count": int(len(y_flat)),
        "valid_count": int(finite_mask.sum()),
        "nan_count": int((~finite_mask).sum()),
        "nan_ratio": float((~finite_mask).mean()) if len(y_flat) > 0 else float("nan"),
        "mean": float(np.mean(valid_y)) if len(valid_y) > 0 else float("nan"),
        "std": float(np.std(valid_y)) if len(valid_y) > 0 else float("nan"),
    }

    print(f"  [generate] {date_str}: rows={len(out_norm)}, valid_label={label_dist['valid_count']}")

    return {
        "lob_rows": len(out_norm),
        "label_rows": len(y_flat),
        "stats_history": stats_history,
        "label_dist": label_dist,
    }


def run_daily_generation(
    daily_chunks: List[Tuple[str, pd.DataFrame]],
    tmp_dir: str,
    stats_history: List[Tuple],
    label_k: int = 100,
    label_h: float = 10.0,
    use_log_return: bool = False,
    split_label: str = "train",
) -> Tuple[List[Dict], List[Tuple]]:
    """
    Generate daily LOB + label files for a list of (date_str, df_day) chunks.
    Returns manifest and updated stats_history.
    """
    day_dir = os.path.join(tmp_dir, split_label)
    os.makedirs(day_dir, exist_ok=True)

    manifest = []

    for i, (date_str, df_day) in enumerate(daily_chunks, start=1):
        lob_path = os.path.join(day_dir, f"{split_label}_lob_{date_str}.parquet")
        label_path = os.path.join(day_dir, f"{split_label}_label_{date_str}.npy")

        print(f"[{split_label}] ({i}/{len(daily_chunks)}) {date_str} ({len(df_day):,} events)")

        result = generate_day_data(
            df_day=df_day,
            date_str=date_str,
            stats_history=stats_history,
            output_lob_parquet=lob_path,
            output_label_npy=label_path,
            label_k=label_k,
            label_h=label_h,
            use_log_return=use_log_return,
        )

        stats_history = result["stats_history"]

        manifest.append({
            "split": split_label,
            "date": date_str,
            "lob_parquet": lob_path,
            "label_npy": label_path,
            "lob_rows": result["lob_rows"],
            "label_rows": result["label_rows"],
            "label_dist": result["label_dist"],
        })

    return manifest, stats_history
